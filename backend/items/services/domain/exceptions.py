"""Доменные исключения."""


class DomainError(Exception):
    """
    Базовое доменное исключение.
    
    Доменные исключения представляют ошибки бизнес-логики.
    Они НЕ должны знать про HTTP — это чистая предметная область.
    """
    pass


class DomainValidationError(DomainError):
    """
    Ошибка валидации бизнес-правил.
    Например: недопустимый переход статуса, нарушение инварианта.
    """
    pass


class DomainConflictError(DomainError):
    """
    Ошибка конфликта состояния.
    Например: объект заблокирован другим пользователем.
    """
    pass

